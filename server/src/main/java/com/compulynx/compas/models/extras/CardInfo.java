package com.compulynx.compas.models.extras;

public class CardInfo {
	private int customers;
	private int braches;
    private int waivedBranches;
    
	public int getCustomers() {
		return customers;
	}
	public void setCustomers(int customers) {
		this.customers = customers;
	}
	public int getBraches() {
		return braches;
	}
	public void setBraches(int braches) {
		this.braches = braches;
	}
	public int getWaivedBranches() {
		return waivedBranches;
	}
	public void setWaivedBranches(int waivedBranches) {
		this.waivedBranches = waivedBranches;
	}
	
  
  
}
